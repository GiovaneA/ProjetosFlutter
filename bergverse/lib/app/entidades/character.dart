import 'dart:typed_data';

class Character {
  int? id;
  String? nome;
  String? img;
  String? idade;
  String? raca;
  String? altura;
  String? genero;
  String? descricao;

  Character(
      {this.id,
        this.nome,
       this.idade,
       this.raca,
       this.altura,
       this.genero,
       this.descricao,
       this.img});

 
}
